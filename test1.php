function quitar_tildes($input){
    $no_permitidas= array ("á","é","í","ó","ú","Á","É","Í","Ó","Ú","ñ","À","Ã","Ì","Ò","Ù","Ã™","Ã ","Ã¨","Ã¬","Ã²","Ã¹","ç","Ç","Ã¢","ê","Ã®","Ã´","Ã»","Ã‚","ÃŠ","ÃŽ","Ã”","Ã›","ü","Ã¶","Ã–","Ã¯","Ã¤","«","Ò","Ã","Ã„","Ã‹");
    $permitidas= array ("a","e","i","o","u","A","E","I","O","U","n","N","A","E","I","O","U","a","e","i","o","u","c","C","a","e","i","o","u","A","E","I","O","U","u","o","O","i","a","e","U","I","A","E");
    
    return str_replace($no_permitidas, $permitidas ,$input);
}

function is_palindrome($input) {
    $input = quitar_tildes($input);
    $input = preg_replace('/[0-9\@\.\;\,\" "]+/', '', str_replace(" ", "", $input));
    $input_invertido = strrev($input);

    if(strcmp(strtolower($input), strtolower($input_invertido))==0)return true;
    return false;
}
